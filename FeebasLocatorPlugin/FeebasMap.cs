    private const int GEN4_MARKER_HEIGHT = 13;

    private static readonly Image Route119 = Properties.Resources.route119;
    private static readonly Image MtCoronet = Properties.Resources.mtcoronet;
    private static readonly SolidBrush MarkerBrush = new(Color.Red);

    private readonly int Generation;
    private readonly Image? Map;
    private ushort[] FeebasTiles = [];
    public uint Seed { get; private set; }

    public FeebasMap(int generation)
    {
        SetStyle(ControlStyles.AllPaintingInWmPaint | ControlStyles.UserPaint | ControlStyles.OptimizedDoubleBuffer, true);

        if (generation is not (3 or 4 or 8))
            throw new ArgumentOutOfRangeException(nameof(generation), $"Generation {generation} is unsupported.");

        Generation = generation;
        Map = Generation == 3 ? Route119 : MtCoronet;
        Size = Map.Size;

        InitializeComponent();
    }

    public void UpdateSeed(uint seed)
    {
        Seed = seed;
        FeebasTiles = Generation == 3 ? Feebas3.GetTiles(Seed) : Feebas4.GetTiles(Seed);
        Invalidate();
    }

    protected override void OnPaint(PaintEventArgs e)
    {
        if (Map != null)
        {
            base.OnPaint(e);

            Graphics g = e.Graphics;
            g.DrawImage(Map, 0, 0);

            if (Generation == 3)
            {
                foreach (var tile in FeebasTiles)
                {
                    if (Feebas3.IsUnderBridge(tile))
                    {
                        for (int i = 0; i < Route119TilesUnderBridge.Length/2; i++)
                        {
                            g.FillRectangle(MarkerBrush, Route119TilesUnderBridge[i, 0], Route119TilesUnderBridge[i, 1], GEN3_MARKER_SIZE, GEN3_MARKER_SIZE);
                        }
                        continue;
                    }

                    if (Feebas3.IsAccessible(tile))
                    {
                        g.FillRectangle(MarkerBrush, Route119Tiles[tile - 4, 0], Route119Tiles[tile - 4, 1], GEN3_MARKER_SIZE, GEN3_MARKER_SIZE);
                    }
                }
            }
            else
            {
                foreach (var tile in FeebasTiles)
                {
                    g.FillRectangle(MarkerBrush, MtCoronetTiles[tile, 0], MtCoronetTiles[tile, 1], GEN4_MARKER_WIDTH, GEN4_MARKER_HEIGHT);
                }
            }
        }
    }

